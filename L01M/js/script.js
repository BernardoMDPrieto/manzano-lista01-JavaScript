//O quadrado da soma dos três valores
var variavelA = parseInt(prompt("Informe a primeira variável"))
var variavelB = parseInt(prompt("Informe a segunda variável"))
var variavelC = parseInt(prompt("Informe a terceira variável"))
var quadradoDaSoma = (variavelA+variavelB+variavelC)**2 
alert(`O quadrado da Soma dos três valores é de ${quadradoDaSoma}`)