j//Soma dos quadrados de três valores
var variavelA = parseInt(prompt("Informe a variável A"))
var variavelB = parseInt(prompt("Informe a variável B"))
var variavelC = parseInt(prompt("Informe a variável C"))
var somaDosQuadrados = (variavelA**2)+(variavelB**2)+(variavelC**2)
alert(`A soma dos quadrados da soma dos três valores é de ${somaDosQuadrados}`)