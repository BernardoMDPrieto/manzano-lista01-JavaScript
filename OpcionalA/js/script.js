//Informar 4 valores, ter o produto do primeiro com o terceiro e a soma do segundo com o quarto
var variavelA = parseInt(prompt("Informe a variável A"))
var variavelB = parseInt(prompt("Informe a variável B"))
var variavelC = parseInt(prompt("Informe a variável C"))
var variavelD = parseInt(prompt("Informe a variável D"))
var produto = variavelA*variavelC
var soma = variavelB+variavelD
alert(`O produto do primeiro e do terceiro valor é de ${produto} \n A soma do segundo com o quarto valor é de ${soma}`)